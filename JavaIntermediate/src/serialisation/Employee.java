package serialisation;

public class Employee {
	int empId;
	String name;
	int salary;
	String designation;
	String email;
	public Employee(int empId, String name, int salary, String designation, String email) {
		super();
		this.empId = empId;
		this.name = name;
		this.salary = salary;
		this.designation = designation;
		this.email = email;
	}
}
